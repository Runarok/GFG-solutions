/**
 * @param {String} inputString - The input string from which alternate characters will be removed.
 * @returns {String} - The modified string containing only characters at even indices.
 */
class Solution {
    delAlternate(inputString) {
        // Convert the string into an array, filter characters at even indices, and join back into a string
        return [...inputString].filter((_, index) => index % 2 === 0).join("");
    }
}
